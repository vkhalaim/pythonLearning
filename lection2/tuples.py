simple_tuple = (0.4, 0.44, 0.22, 0.47, 0.62, 0.14, 0.11, 0.26, 0.41, 0.69)

# the most easier way to sort tuple and find max and min elements
simple_tuple_sorted = sorted(simple_tuple)

print("Max_elem: ", simple_tuple_sorted[len(simple_tuple_sorted) - 1])
print("Min_elem: ", simple_tuple_sorted[0])
